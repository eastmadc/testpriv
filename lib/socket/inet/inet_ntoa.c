/*
 * Convert network-format internet address
 * to base 256 d.d.d.d representation.
 */
/*const*/ char *
inet_ntoa(struct in_addr in) {
	static char ret[18];

	strlcpy(ret, "[inet_ntoa error]", sizeof(ret));
	(void) inet_ntop(AF_INET, &in, ret, sizeof ret);
	return (ret);
}

